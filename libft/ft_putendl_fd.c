#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	if (s != NULL)
	{
		i = 0;
		while (s[i])
			i++;
		write(fd, s, i);
		write(fd, "\n", 1);
	}
}
